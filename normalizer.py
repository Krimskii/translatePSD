def normalize(text):
    t = str(text)

    # CAD / local terminology cleanup
    for source in sorted(TERM_REPLACEMENTS, key=len, reverse=True):
        t = t.replace(source, TERM_REPLACEMENTS[source])

    # note-style wording common in local design docs
    t = re.sub(r"^также можно\b", "допускается", t, flags=re.IGNORECASE)
    t = re.sub(r"^можно\b", "допускается", t, flags=re.IGNORECASE)
    t = re.sub(r"\bне менее\b", "не менее", t, flags=re.IGNORECASE)

    # диаметр
    t = re.sub(r"[φΦфФ]", "Ø", t)

    # DN
    t = re.sub(r"DN\s*?(\d+)", r"DN\1", t)

    # размеры 300x200
    t = re.sub(r"(\d+)\s*[xхXХ]\s*(\d+)", r"\1×\2", t)

    # H=3000 / L=6000
    t = re.sub(r"H\s*=\s*(\d+)", r"H=\1", t)
    t = re.sub(r"L\s*=\s*(\d+)", r"L=\1", t)

    # мм / метры
    t = re.sub(r"(\d+)\s*mm", r"\1 мм", t)
    t = re.sub(r"(\d+)мм", r"\1 мм", t)
    t = re.sub(r"(\d+(\.\d+)?)\s*m\b", r"\1 м", t)

    # масштаб
    t = re.sub(r"比例\s*(\d+)\s*[:：]\s*(\d+)", r"М \1:\2", t)
    t = re.sub(r"масштаб\s*(\d+)\s*[:：]\s*(\d+)", r"М \1:\2", t, flags=re.IGNORECASE)

    # отметки уровня
    t = re.sub(r"\+?0\.000", "±0.000", t)

    # инженерные сокращения
    t = t.replace("отметка", "отм.")
    t = t.replace("отметки", "отм.")
    t = t.replace("наружный диаметр", "Ø")
    t = t.replace("глубина ", "Глубина ")
    t = t.replace("шириной не менее", "шириной не менее")

    # труба Ø273 / DN300 мм / 300×200 мм
    t = re.sub(r"Ø\s*(\d+)", r"Ø\1", t)
    t = re.sub(r"DN(\d+)\s*мм", r"DN\1 мм", t)
    t = re.sub(r"(\d+)×(\d+)\s*мм", r"\1×\2 мм", t)

    # local phrasing for notes
    t = t.replace("высота установки", "H=")
    t = t.replace("длина", "L=")
    t = t.replace("квадратный колодец квадратный колодец", "квадратный колодец")
    t = t.replace("круглый колодец круглый колодец", "круглый колодец")
    t = t.replace("по грани стальной колонны грани", "по грани стальной колонны")
    t = t.replace("привязать привязать", "привязать")
    t = t.replace("допускается устройство допускается", "допускается устройство")

    if t and t[0].islower():
        t = t[0].upper() + t[1:]

    # двойные пробелы
    t = re.sub(r"\s+", " ", t)

    return t.strip()


def normalize_df(df):
    source = "translated" if "translated" in df.columns else "text"
    df["normalized"] = df[source].apply(normalize)
    return df
